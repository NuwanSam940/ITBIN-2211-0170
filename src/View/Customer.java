    private void showAddCustomers() {
        try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/flowermanagement", "root", "");
        String query = "SELECT * FROM customers";
        PreparedStatement pst = con.prepareStatement(query);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1_Display_User.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            Object[] row = {rs.getString("Name"), rs.getString("Address"), rs.getString("PhoneNo")};
            model.addRow(row);
        }

        pst.close();
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }

