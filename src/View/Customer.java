    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1_Display_User.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to update.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String newName = Name.getText();
            String newAddress = Address.getText();
            String newContactNo = PhoneNo.getText();

            if (newName.isEmpty() || newAddress.isEmpty() || newContactNo.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                try {
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/flowermanagement", "root", "");
                    String query = "UPDATE customers SET Name=?, Address=?, PhoneNo=? WHERE Name=?";
                    PreparedStatement pst = con.prepareStatement(query);
                    pst.setString(1, newName);
                    pst.setString(2, newAddress);
                    pst.setString(3, newContactNo);
                    pst.setString(4, jTable1_Display_User.getValueAt(row, 0).toString());
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Update successful.");
                    showAddCustomers();
                    resetFields();
                    pst.close();
                    con.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1_Display_User.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    Class.forName("com.mysql.jdbc.Driver");
                    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/flowermanagement", "root", "")) {
                        String query = "DELETE FROM customers WHERE Name = ?";
                        PreparedStatement pst = con.prepareStatement(query);
                        pst.setString(1, jTable1_Display_User.getValueAt(row, 0).toString()); // Assuming Name is in the first column
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Delete successful");
                        showAddCustomers();
                        resetFields();
                        pst.close();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

