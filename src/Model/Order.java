public class Order {
    
    
    private String OrID;
    private String OrCuName;
    private String OrCuNo;
    private String OrFName;
    private String OrQuantity;

    public String getOrID() {
        return OrID;
    }

    public void setOrID(String OrID) {
        this.OrID = OrID;
    }

    public String getOrCuName() {
        return OrCuName;
    }

    public void setOrCuName(String OrCuName) {
        this.OrCuName = OrCuName;
    }

    public String getOrCuNo() {
        return OrCuNo;
    }

    public void setOrCuNo(String OrCuNo) {
        this.OrCuNo = OrCuNo;
    }

    public String getOrFName() {
        return OrFName;
    }

    public void setOrFName(String OrFName) {
        this.OrFName = OrFName;
    }

    public String getOrQuantity() {
        return OrQuantity;
    }

    public void setOrQuantity(String OrQuantity) {
        this.OrQuantity = OrQuantity;
    }
    
    
    
    
    
}
